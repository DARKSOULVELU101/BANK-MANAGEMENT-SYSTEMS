#CREATE DATABASE BANK MANAGEMENT SYSTEM (PROJECT)

#INSERT THE DATAS TO THE DATABASE
#CREATING THE TABLES FOR COSTUMER
CREATE TABLE Customers (
customer_id INT PRIMARY KEY,
name VARCHAR(50),
address VARCHAR(100),
phone VARCHAR(15),
email VARCHAR(50)
);
#CREATE TABLE FOR BRANCH
CREATE TABLE Branch (
branch_id INT PRIMARY KEY,
branch_name VARCHAR(50),
location VARCHAR(50)
);
#CREATE TABLES FOR ACCOUNTS
CREATE TABLE Accounts (
account_id INT PRIMARY KEY,
customer_id INT,
branch_id INT,
account_type VARCHAR(20),
balance DECIMAL(10,2),
FOREIGN KEY(customer_id) REFERENCES Customers(customer_id),
FOREIGN KEY(branch_id) REFERENCES Branch(branch_id)
);
#CREATE TABLES FOR TRANSACTION 
CREATE TABLE Transactions (
transaction_id INT PRIMARY KEY,
account_id INT,
transaction_type VARCHAR(20),
amount DECIMAL(10,2),
transaction_date DATE,
FOREIGN KEY(account_id) REFERENCES Accounts(account_id)
);
#CREATE TABLES FOR LOAN
CREATE TABLE Loans (
loan_id INT PRIMARY KEY,
customer_id INT,
loan_amount DECIMAL(10,2),
loan_type VARCHAR(50),
loan_status VARCHAR(20),
FOREIGN KEY(customer_id) REFERENCES Customers(customer_id)
);
DESCRIBE LOANS;
